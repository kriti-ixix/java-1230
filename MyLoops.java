class MyLoops
{
    public static void main(String[] args)
    {
        // While Loop
        int i = 0;
        while (i < 11)
        {
            System.out.println(i);
            i++;
        }

        // For Loops
        for (int i = 0; i < 11; i++)
        {
            System.out.println(i);
        }

        // Do While Loop
        int i = 10;
        do
        {
            System.out.println(i);
            i++;
        } while (i < 5);
    }
}